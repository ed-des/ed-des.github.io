function collapseDesc() {
    if (  $( '#desc.collapsible' ).hasClass( 'expand' ) ) {
        $( '#desc.collapsible' ).removeClass( 'expand' );
    } else {
        $( '#desc.collapsible' ).addClass( 'expand' );
    }
}
